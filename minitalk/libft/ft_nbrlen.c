int	ft_nbrlen(int n)
{
	int	i;
	int	nb;

	nb = n;
	if (nb < 0)
		nb = -nb;
	i = 0;
	while (nb != 0)
	{
		nb /= 10;
		i++;
	}
	if (n < 0)
		return (i + 1);
	else
		return (i);
}
